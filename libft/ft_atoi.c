int	ft_atoi(const char *str)
{
	int	a;
	int	b;
	int	c;

	a = 0;
	b = 1;
	c = 0;
	while (str[a] == '\t' || str[a] == '\r' || str[a] == ' '
		|| str[a] == '\f' || str[a] == '\v' || str[a] == '\n')
		a++;
	if (str[a] == '+' || str[a] == '-')
	{
		if (str[a] == '-')
			b *= -1;
		a++;
	}
	while (str[a] >= '0' && str[a] <= '9')
	{
		c = c * 10 + (str[a] - '0');
		a++;
	}
	return (c * b);
}
